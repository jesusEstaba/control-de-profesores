    void mostrardatos() throws SQLException{
        DefaultTableModel modelo=new DefaultTableModel();
        modelo.addColumn("Nombre");
        modelo.addColumn("Fecha");
        modelo.addColumn("Tipo");
        tablaregistro.setModel(modelo);
        
        String []datos=new String[3];
        
        conxClass con=new conxClass();
        Connection reg=con.conexion();
        Statement nigga=reg.createStatement();
        ResultSet rs=nigga.executeQuery("select * from registro");
        while(rs.next()){
            datos[0]=Nombrereturn(rs.getString("cedula"));
            datos[1]=rs.getString("fecha");
            datos[2]=rs.getString("tipo");
            modelo.addRow(datos);
        }
        tablaregistro.setModel(modelo);
        
    }
    
    private String Nombrereturn(String cedulanum){
    String Cadena="";
         try{
       conxClass con=new conxClass();
       Connection reg=con.conexion();
       Statement nigga=reg.createStatement();
       
       ResultSet rs =  nigga.executeQuery("SELECT * FROM profesores where cedula='"+cedulanum+"'");
          
       while (rs.next())
       {
          Cadena=rs.getObject("nombre")+
             " "+rs.getObject("apellido");
       }
       rs.close();
       }
       catch(SQLException e){
           System.out.println("error: "+e);
       }
       return Cadena;
 }

    
