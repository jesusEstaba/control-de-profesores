     private String Nombrereturn(String cedulanum){
    String Cadena="";
         try{
       conxClass con=new conxClass();
       Connection reg=con.conexion();
       Statement nigga=reg.createStatement();
       
       ResultSet rs =  nigga.executeQuery("SELECT * FROM profesores where cedula='"+cedulanum+"'");
          
       while (rs.next())
       {
          Cadena=rs.getObject("nombre")+
             " "+rs.getObject("apellido");
       }
       rs.close();
       }
       catch(SQLException e){
           System.out.println("error: "+e);
       }
       return Cadena;
 }
    
    
    
    
    
    
    
    
    
