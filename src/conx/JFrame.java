    private boolean Entro(){
        boolean band=true;
        conxClass con=new conxClass();
        Connection reg=con.conexion();
        
        try{
        
            Statement nigga=reg.createStatement();
            ResultSet rs = nigga.executeQuery("SELECT * FROM dentro WHERE cedula='"+F_Cedula.getText()+"'");
            String Name="";
            while (rs.next())
            {
               Name=""+rs.getObject("cedula");
            }
            rs.close();

            if(Name.compareTo("")!=0)
            {
                band=false;
            }
        }
        catch(SQLException e){
            System.out.println("error: "+e);
        }
        
        return band;
    }
    
    private void add(String tipo){
        conxClass con=new conxClass();
        Connection reg=con.conexion();
        String sql,cedula=F_Cedula.getText();
        java.util.Date fecha = new Date();
        Format formatter = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
        String s = formatter.format(fecha);
        sql="INSERT INTO registro(cedula,fecha,tipo)values(?,?,?)";
            try{
                PreparedStatement pst=reg.prepareStatement(sql);
                pst.setString(1, cedula);
                pst.setString(2, s);
                pst.setString(3, tipo);
                pst.executeUpdate();
            }
            catch(SQLException e){
                System.out.println("Error add: "+e);
            }
    }
    
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if(F_Cedula.getText().compareTo("")!=0)
        {
            conxClass con=new conxClass();
            Connection reg=con.conexion();
            try{

                Statement nigga=reg.createStatement();
                ResultSet rs = nigga.executeQuery("SELECT * FROM profesores WHERE cedula = '"+F_Cedula.getText()+"'");
                String Name="";
                while (rs.next())
                {
                   Name=""+rs.getObject("nombre");
                }
                rs.close();

                if(Name.compareTo("")!=0)
                {
                    if(Entro())
                    {
                        String sql,cedula=F_Cedula.getText();
                        sql="INSERT INTO dentro(cedula)values(?)";
                        try{
                            PreparedStatement pst=reg.prepareStatement(sql);

                            pst.setString(1, cedula);
                            int n=pst.executeUpdate();
                            if(n>0)
                            {
                                JOptionPane.showMessageDialog(rootPane, "Bienvenido Profesor "+Name);
                                add("Entrada");
                                F_Cedula.setText("");
                            }
                            else
                                JOptionPane.showMessageDialog(rootPane, "Eror entrada");
                        }
                        catch(SQLException e){
                            System.out.println("Error: "+e);
                        }
                    }
                    else
                    {
                        //borrar registro
                        String query = "delete from dentro where cedula = ?";
                        PreparedStatement preparedStmt = reg.prepareStatement(query);
                        preparedStmt.setInt(1, Integer.parseInt(F_Cedula.getText()));

                        // execute the preparedstatement
                        preparedStmt.execute();
                        JOptionPane.showMessageDialog(rootPane, "Hasta Luego Profesor "+Name);
                        add("Salida");
                        F_Cedula.setText("");
                    }
                        
                }
                else
                {
                    JOptionPane.showMessageDialog(rootPane, "No Existe");
                }

            }
            catch(SQLException e){
                System.out.println("error: "+e);
            }
        }
        else
        {
            JOptionPane.showMessageDialog(rootPane,"Campo Vacio");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

